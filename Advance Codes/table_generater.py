# If you have troble in learning/writing multiplication tables of any specific number, You can use this code to make any number's table in 1 click! 
# Try Now!

n = int(input("Enter a number to make a table of that number: "))

i = 1

while i <= 10:
    print(n,"x",i,"=",n * i)

    i += 1 

